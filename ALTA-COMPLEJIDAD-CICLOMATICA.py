def alta_complejidad():
    for i in range(1000):
        for j in range(1000):
            if i == j:
                print("i es igual a j")
            elif i < j:
                print("i es menor que j")
            else:
                print("i es mayor que j")
    if i == 0:
        print("i es igual a 0")
    elif i < 0:
        print("i es menor que 0")
    else:
        print("i es mayor que 0")


